/**
 * Vercel serverless function: POST /api/generate-contract
 * Returns a Utah-focused draft purchase agreement built from user inputs.
 * NOTE: This is a draft for discussion only, not legal advice or a state-approved form.
 */

function parseMoney(raw) {
  if (!raw) return { value: null, label: '' };
  const cleaned = String(raw).replace(/[^0-9.]/g, '');
  const num = parseFloat(cleaned);
  if (!isFinite(num)) return { value: null, label: '' };
  return { value: num, label: `$${num.toLocaleString(undefined, { maximumFractionDigits: 0 })}` };
}

function parsePercent(raw) {
  if (!raw) return null;
  const cleaned = String(raw).replace(/[^0-9.]/g, '');
  const num = parseFloat(cleaned);
  return isFinite(num) ? num : null;
}

function buildUtahContract(body = {}) {
  const state = (body.state || 'UT').toUpperCase();
  const forUtah = state === 'UT';
  const headingState = forUtah ? 'Utah' : 'Residential';
  const propertyAddress = body.propertyAddress || '______';
  const buyerName = body.buyerName || 'Buyer';
  const price = parseMoney(body.offerPrice);
  const earnestPct = parsePercent(body.earnestPercent) ?? 2;
  const earnestVal = price.value ? Math.round(price.value * (earnestPct / 100)) : null;
  const earnestLabel = earnestVal
    ? `$${earnestVal.toLocaleString(undefined, { maximumFractionDigits: 0 })} (${earnestPct}% of purchase price)`
    : `${earnestPct}% of purchase price`;
  const closingDate = body.closingDate || '____';
  const financingDays = body.financingDays ? String(body.financingDays) : '21';
  const otherContingencies = body.otherContingencies || 'Inspection, appraisal, and any additional contingencies mutually agreed by Buyer and Seller in writing.';

  const disclaimerTop = `<div style="font-family:var(--sans);font-size:11px;color:var(--rust);margin-bottom:12px;"><strong>IMPORTANT:</strong> This is a draft purchase agreement for discussion only. It is <strong>not</strong> a state-approved form and <strong>not</strong> legal advice. Always have a licensed real estate attorney or real estate professional in your state review and prepare the final documents before signing.</div>`;

  const disclaimerBottom = `<p style="margin-top:18px;font-size:12px;color:var(--muted);"><strong>Disclaimer:</strong> This draft is generated by software for informational purposes only and may omit provisions required under ${headingState} law or your specific transaction. Do not rely on this document as-is. Have a licensed attorney review, modify, and approve any contract before use.</p>`;

  return {
    contractText: `
${disclaimerTop}
<p>This <strong>${forUtah ? 'Utah Residential Real Estate Purchase Agreement' : 'Residential Real Estate Purchase Agreement'}</strong> (the "Agreement") is entered into as of the Effective Date below by and between <strong>Seller</strong> and <strong>${buyerName}</strong> ("Buyer").</p>

<p><strong>1. Property.</strong> Seller agrees to sell to Buyer, and Buyer agrees to purchase from Seller, the real property commonly known as <strong>${propertyAddress}</strong>, together with all improvements, fixtures, and appurtenances (the "Property"). A full legal description may be attached as an exhibit in the final attorney-reviewed version.</p>

<p><strong>2. Purchase Price &amp; Earnest Money.</strong> The total purchase price for the Property is <strong>${price.label || '$_____'} (the "Purchase Price")</strong>, payable as follows:</p>
<ul>
  <li><strong>Earnest Money:</strong> Buyer shall deposit approximately <strong>${earnestLabel}</strong> as earnest money with the designated escrow/title company within three (3) business days after acceptance.</li>
  <li><strong>Balance at Closing:</strong> The remainder of the Purchase Price shall be paid in cash or immediately available funds at Closing, subject to prorations and adjustments.</li>
</ul>

<p><strong>3. Financing.</strong> This Agreement is ${price.value ? 'intended to be' : 'typically'} contingent upon Buyer obtaining acceptable financing. Buyer shall apply for financing promptly and use good faith efforts to obtain loan approval. Unless otherwise agreed, Buyer shall have approximately <strong>${financingDays}</strong> days from the Effective Date to secure loan approval or waive this contingency in writing.</p>

<p><strong>4. Closing &amp; Possession.</strong> Closing ("Closing") shall occur on or before <strong>${closingDate}</strong>, or such other date as the parties may mutually agree in writing. Possession of the Property shall be delivered to Buyer at Closing, subject to any written rent-back or occupancy agreement between the parties.</p>

<p><strong>5. Inspections &amp; Due Diligence.</strong> Buyer is strongly encouraged to conduct all inspections, investigations, and due diligence Buyer deems necessary, including but not limited to general home inspection, roof, structural, pest, environmental, and HOA review (if applicable). Unless otherwise stated in the final contract, Seller is not making any warranties beyond those expressly set forth in the written Agreement.</p>

<p><strong>6. Contingencies.</strong> In addition to any financing contingency described above, the parties acknowledge the following common contingencies, which should be confirmed, modified, or removed by a licensed attorney in the final contract:</p>
<ul>
  <li>Inspection and due diligence contingencies.</li>
  <li>Appraisal contingency (property to appraise at or above Purchase Price, or as otherwise agreed).</li>
  <li>Title and escrow review, including clearance of liens and encumbrances.</li>
  <li>Any additional contingencies specific to this transaction, for example: <em>${otherContingencies}</em>.</li>
</ul>

<p><strong>7. Title, Closing Costs &amp; Prorations.</strong> A licensed title/escrow company in ${headingState} should prepare the final closing documents. The parties should agree, in the final contract, how title insurance premiums, escrow fees, transfer taxes, HOA fees, and property tax prorations will be allocated between Buyer and Seller in accordance with local practice.</p>

<p><strong>8. Disclosures.</strong> Seller shall provide Buyer with any disclosures required under ${headingState} law (and federal law), including but not limited to property condition disclosures, lead-based paint disclosures (if applicable), and HOA documents, within the time periods required by statute or agreed in the final contract.</p>

<p><strong>9. Default &amp; Dispute Resolution.</strong> The final attorney-prepared contract should specify remedies available to Buyer and Seller in the event of default (e.g., forfeiture of earnest money, specific performance, or other remedies), as well as the agreed method of dispute resolution (mediation, arbitration, court, venue, and governing law).</p>

<p><strong>10. Entire Agreement &amp; Signatures.</strong> The final written purchase agreement prepared or reviewed by a licensed attorney, together with all properly executed addenda and exhibits, will constitute the entire agreement between the parties with respect to the sale of the Property. Electronic or counterpart signatures may be permitted if allowed under applicable law.</p>

${disclaimerBottom}
`
  };
}

export default function handler(req, res) {
  res.setHeader('Access-Control-Allow-Origin', '*');
  res.setHeader('Access-Control-Allow-Methods', 'POST, OPTIONS');
  res.setHeader('Access-Control-Allow-Headers', 'Content-Type');

  if (req.method === 'OPTIONS') return res.status(204).end();
  if (req.method !== 'POST') return res.status(405).json({ error: 'Method not allowed' });

  const body = typeof req.body === 'string' ? JSON.parse(req.body || '{}') : (req.body || {});
  // For now we only have a Utah-focused / generic residential draft.
  const draft = buildUtahContract(body);
  return res.status(200).json(draft);
}
